// 1. Npm understanding
// npm - node package manager
// node js core installed are module and download by npm are package

// ===========================================================

// 2. installing and uninstalling packages
// install - npm install/i package-name @<version>
// uninstall - npm uninstall package-name

// ===========================================================
// 3. understanding node_modules and package-lock.json
// node_modules - folder where all the downloaded packages are stored
// package-lock.json - file that contains the exact version of each package and its dependencies, ensuring consistent installs across different environments

// 4. dependencies and dev dependencies
//  dependencies - packages required for the application to run in production
// dev dependencies - packages required for development and testing, but not needed in production
// ===========================================================

// 5. scripts-understanging default scripts PATH and custom scripts
// default scripts - start, test, build, etc. can be run using npm start, npm test, etc.
// custom scripts - can be defined in package.json under "scripts" and run using npm run script-name
// ===========================================================
